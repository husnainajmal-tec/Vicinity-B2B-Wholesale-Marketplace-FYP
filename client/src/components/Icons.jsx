/**
 * Map a product-category name to a reasonable icon component.
 * Falls back to a generic Package icon.
 */
export function iconForCategory(name = "") {
  const map = {
    "Textiles & Apparel": Shirt,
    Electronics: Cpu,
    "Food & Beverage": Utensils,
    "Industrial & Machinery": Factory,
    Packaging: Box,
    "Construction Materials": HardHat,
    "Health & Beauty": Sparkles,
    Agriculture: Leaf,
    "Home & Furniture": Sofa,
    Other: Package,
  };
  return map[name] || Package;
}
